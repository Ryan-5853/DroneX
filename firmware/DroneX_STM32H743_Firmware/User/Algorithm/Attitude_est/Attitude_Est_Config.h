/* ============================================================================
 * 一、参考系定义：主板系（Motherboard Frame）
 * ============================================================================
 *
 * IMU 与主板固定，主板与机体固定方式暂不确定，算法层以主板为参考系输出姿态。
 *
 * 主板平放时（水平放置）：
 *   - X 轴：水平向右
 *   - Y 轴：水平向前
 *   - Z 轴：竖直向上
 *   - 右手系
 *
 * 欧拉角约定（rad）：
 *   - yaw：绕 Z 轴旋转（航向）
 *   - pitch：绕 X 轴旋转（俯仰）
 *   - roll：绕 Y 轴旋转（滚转）
 *   - 旋转顺序：Z → X → Y（即先 yaw，再 pitch，再 roll）
 *
 * 解算约定（统一按轴上加速度、绕轴角速度）：
 *   - 加速度：Z 轴朝上时静止期望 [0, 0, -g]，即重力方向沿 -Z
 *   - 角速度：右手定则，绕轴正方向由右手拇指指向轴正方向时四指弯曲方向
 */

